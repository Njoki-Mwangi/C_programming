#include<stdio.h>
int main()
{
char ch = 'B';
printf("%c\n", ch); //prints character value
int x = 67;
int  y = 89;
printf("%d\n",x);
printf("%i\n",y);
//print decimal or integer data using d and i
float f = 26.4678955;
printf("%f\n", f);
//print float value
printf("%e\n", f);
//print scientific value 
int a = 57;
printf("%o\n", a);
//print octal value
printf("%x\n", a);
//print hex format
char str[] = "Hello World";
printf("%s\n", str);
return 0;
}
